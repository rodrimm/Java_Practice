    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombrePerro = txtNombrePerro.getText();
        String raza = txtRaza.getText();
        String color = txtColor.getText();
        String alergico = cmbAlergico.getSelectedItem().toString();
        String atEspecial = cmbAtEspecial.getSelectedItem().toString();
        String nombreDuenio = txtNombreDuenio.getText();
        String celDuenio = txtCelDuenio.getText();
        String observaciones = txtObservaciones.getText();

        control.altaMascota(nombrePerro, raza, color, alergico, atEspecial, nombreDuenio, celDuenio, observaciones);

        JOptionPane.showMessageDialog(rootPane, "Mascota Agregada", "Creada", JOptionPane.INFORMATION_MESSAGE);

        txtNombrePerro.setText("");
        txtRaza.setText("");
        txtColor.setText("");
        cmbAlergico.setSelectedItem("-");
        cmbAtEspecial.setSelectedItem("-");
        txtNombreDuenio.setText("");
        txtCelDuenio.setText("");
        txtObservaciones.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

